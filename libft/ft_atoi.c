#include "libft.h"

/*
** The atoi() function converts the initial portion of the string pointed to
** by str to int representation.
*/

int		ft_atoi(const char *str)
{
	int	res;
	int	sign;
	int	i;

	res = 0;
	sign = 1;
	i = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (ft_isdigit(str[i]))
		res = res * 10 + str[i++] - '0';
	return (sign * res);
}
