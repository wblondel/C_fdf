#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *first;

	first = dst;
	while (len-- && *src)
		*dst++ = *src++;
	len++;
	while (len--)
		*dst++ = '\0';
	return (first);
}
